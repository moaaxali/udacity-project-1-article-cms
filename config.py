import os
from azure.keyvault.secrets import SecretClient
from azure.identity import DefaultAzureCredential

keyVaultName = os.environ["KEY_VAULT_NAME"]
KVUri = f"https://{keyVaultName}.vault.azure.net"

credential = DefaultAzureCredential()
_client = SecretClient(vault_url=KVUri, credential=credential)

basedir = os.path.abspath(os.path.dirname(__file__))

class Config(object):
    SECRET_KEY = os.environ.get('SECRET_KEY') or 'secret-key'

    BLOB_ACCOUNT = _client.get_secret('blob-account').value
    BLOB_STORAGE_KEY = _client.get_secret('blob-storage-key').value
    BLOB_CONTAINER = _client.get_secret('blob-container').value

    SQL_SERVER = _client.get_secret('sql-server').value
    SQL_DATABASE = _client.get_secret('sql-database').value
    SQL_USER_NAME = _client.get_secret('sql-username').value
    SQL_PASSWORD = _client.get_secret('sql-pass').value
    # Below URI may need some adjustments for driver version, based on your OS, if running locally
    SQLALCHEMY_DATABASE_URI = 'mssql+pyodbc://' + SQL_USER_NAME + '@' + SQL_SERVER + ':' + SQL_PASSWORD + '@' + SQL_SERVER + ':1433/' + SQL_DATABASE  + '?driver=ODBC+Driver+17+for+SQL+Server'
    SQLALCHEMY_TRACK_MODIFICATIONS = False

    ### Info for MS Authentication ###
    ### As adapted from: https://github.com/Azure-Samples/ms-identity-python-webapp ###
    CLIENT_SECRET = _client.get_secret('client-secret').value
